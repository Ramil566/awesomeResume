    public void fillTable(){
        Vector<Vector> rows=new Vector<Vector>();
        
        for(EmploymentHistory EH:employmentHistory){
            Vector row=new Vector();
            row.add(EH.getHeader());
            row.add(EH.getBeginDate());
            row.add(EH.getEndDate());
            row.add(EH.getJobDescription());
            rows.add(row);
        }
        
        DefaultTableModel DTM=new DefaultTableModel(rows,new Vector(Arrays.asList("Header","Begin Date","End Date","Job Description")));

        tblEmploymentHistory.setModel(DTM);
        
    }
    
    


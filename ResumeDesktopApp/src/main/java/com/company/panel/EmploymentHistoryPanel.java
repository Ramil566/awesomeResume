    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String header=txtHeader.getText();
        String beginDateStr=txtBeginDate.getText();
        Date beginDate=null;
        try{
            beginDate=sdf.parse(beginDateStr);
        }catch(Exception e){e.printStackTrace();}
        
        String endDateStr=txtEndDate.getText();
        Date endDate=null;
        try{
            endDate=sdf.parse(endDateStr);
        }catch(Exception e){e.printStackTrace();}
        
        String jobDescription=txtJobDescription.getText();

        java.sql.Date b=new java.sql.Date(beginDate.getTime());
        java.sql.Date e=new java.sql.Date(endDate.getTime());
        
        employmentHistory.add(new EmploymentHistory(null,header,b,e,jobDescription,loggedInUser));
        fillTable();
    }//GEN-LAST:event_btnAddActionPerformed

